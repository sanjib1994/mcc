package com.salk.mycircadianclock.history.history_activity;

public class ActivityListModel {

    String day_range = "";
    String step_count = "";
    String sleep_duration = "";
    String exercise_duration = "";
    String step_days = "";
    String sleep_days = "";
    String exercise_days = "";


    public String getDay_range() {
        return day_range;
    }

    public void setDay_range(String day_range) {
        this.day_range = day_range;
    }

    public String getStep_count() {
        return step_count;
    }

    public void setStep_count(String step_count) {
        this.step_count = step_count;
    }

    public String getSleep_duration() {
        return sleep_duration;
    }

    public void setSleep_duration(String sleep_duration) {
        this.sleep_duration = sleep_duration;
    }

    public String getExercise_duration() {
        return exercise_duration;
    }

    public void setExercise_duration(String exercise_duration) {
        this.exercise_duration = exercise_duration;
    }

    public String getStep_days() {
        return step_days;
    }

    public void setStep_days(String step_days) {
        this.step_days = step_days;
    }

    public String getSleep_days() {
        return sleep_days;
    }

    public void setSleep_days(String sleep_days) {
        this.sleep_days = sleep_days;
    }

    public String getExercise_days() {
        return exercise_days;
    }

    public void setExercise_days(String exercise_days) {
        this.exercise_days = exercise_days;
    }
}
